#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

/** \brief La funcion suma dos valores reales.
 *
 * \param varUno carga el primer valor de la operacion.
 * \param varDos carga el segundo valor de la operacion.
 * \return El resultado de la operacion.
 *
 */
float Suma(float varUno, float varDos);

/** \brief La funcion resta dos valores reales.
 *
 * \param varUno carga el primer valor de la operacion.
 * \param varDos carga el segundo valor de la operacion.
 * \return El resultado de la operacion.
 *
 */
float Resta(float varUno, float varDos);

/** \brief La funcion divide dos valores reales.
 *
 * \param varUno carga el primer valor de la operacion.
 * \param varDos carga el segundo valor de la operacion.
 * \return El resultado de la operacion.
 *
 */
 float Division(float varUno, float varDos);

 /** \brief La funcion multiplica dos valores reales.
 *
 * \param varUno carga el primer valor de la operacion.
 * \param varDos carga el segundo valor de la operacion.
 * \return El resultado de la operacion.
 *
 */
 float Multiplicacion(float varUno, float varDos);

 /** \brief La funcion realiza el factorial de un numero real.
  *
  * \param varUno carga el valor de la operacion.
  * \return El resultado de la operacion.
  *
  */
  int Factorial(float varUno);

#endif // FUNCIONES_H_INCLUDED
